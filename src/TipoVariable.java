public class TipoVariable {
    public static void main(String[] args){
        System.out.println("Tipos de variables: ");
        System.out.println(" ");
        System.out.println("Tipo int: ");
        int edad = 23;
        System.out.println(edad);
        System.out.println("Como tengo declarada la variable 'edad' puedo asignar otros valores a la variable sin indicar el tipo de datos: ");
        edad = 190;
        System.out.println(edad);
        System.out.println("Suma de valores 190 + 150, utilizando la variable edad: ");
        edad = 190 + 150;
        System.out.println("La suma de los valores es: " + edad);
        System.out.println(" ");
        System.out.println("Tipo double: ");
        double decimal = 15.0;
        System.out.println(decimal);

    }
}
