public class ConvertDato {
    public static void main(String[] args){
        double variable1 = 253.32;
        System.out.println("Varible doble"+variable1);
        int variable1Entero = (int) variable1;
        System.out.println("Varible double pero int");
        System.out.println(variable1Entero);

        System.out.println("Tipos de variables: ");
        System.out.println("Tipo Long");
        long prueba = 123131321232193L;
        System.out.println(prueba);
        System.out.println("Tipo short");
        short prueba1 = 12333;
        System.out.println(prueba1);
        System.out.println("Tipo byte");
        byte prueba2 = 15;
        System.out.println(prueba2);
        System.out.println("Tipo float");
        float prueba3 = 2.3F;
        System.out.println(prueba3);

    }
}
